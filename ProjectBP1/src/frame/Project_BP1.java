    private int searchQueryRows() {
    String sql = "SELECT * FROM barang WHERE "
            + "(? = '' OR LOWER(nama_barang) LIKE ?) AND "
            + "(? = '' OR LOWER(kategori) LIKE ?) AND "
            + "(? = '' OR LOWER(harga) LIKE ?) AND "
            + "(? = '' OR LOWER(jumlah_stok) LIKE ?)";

    int result = 0;

    
        try {

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        prep = connector.connect.prepareStatement(sql);

        prep.setString(1, namaBarangField.getText());
        prep.setString(2, "%" + namaBarangField.getText() + "%");
        prep.setString(3, kategoriField.getText());
        prep.setString(4, "%" + kategoriField.getText() + "%");
        prep.setString(5, hargaField.getText());
        prep.setString(6, "%" + hargaField.getText() + "%");
        prep.setString(7, jumlahStokField.getText());
        prep.setString(8, "%" + jumlahStokField.getText() + "%");

        rs = prep.executeQuery();

        while (rs.next()) {
            String id = rs.getString("id");
            String nama_barang = rs.getString("nama_barang");
            String kategori = rs.getString("kategori");
            String harga = rs.getString("harga");
            String jumlah_stok = rs.getString("jumlah_stok");

            model.addRow(new Object[]{id, nama_barang, kategori, harga, jumlah_stok});
            result = 1;
        }

    }
    catch (Exception err

    
        ) {
            JOptionPane.showMessageDialog(this, err);
    }

    return result ;
}

