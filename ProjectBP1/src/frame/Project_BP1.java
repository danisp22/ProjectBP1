    private void load() {
        String sql = "SELECT * FROM barang";

        try {
            st = connector.connect.createStatement();
            rs = st.executeQuery(sql);

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (rs.next()) {
                int column1 = rs.getInt("id");
                String column2 = rs.getString("nama_barang");
                String column3 = rs.getString("kategori");
                Float column4 = rs.getFloat("harga");
                int column5 = rs.getInt("jumlah_stok");

                model.addRow(new Object[]{column1, column2, column3, column4, column5});

            }
        } catch (Exception err) {
            JOptionPane.showMessageDialog(null, err);
        }
    }

    private void create() {
        String sql = "INSERT INTO barang (nama_barang, kategori, harga, jumlah_stok) VALUES(?, ?, ?, ?)";
        try {
            prep = connector.connect.prepareStatement(sql);

            prep.setString(1, namaBarangField.getText());
            prep.setString(2, kategoriField.getText());
            prep.setFloat(3, Float.parseFloat(hargaField.getText()));
            prep.setInt(4, Integer.parseInt(jumlahStokField.getText()));

            int rowsUpdated = prep.executeUpdate();

            if (rowsUpdated > 0) {
                JOptionPane.showMessageDialog(null, "Data berhasil diinput.");
            } else {
                JOptionPane.showMessageDialog(null, "Maaf, data gagal diinput.");
            }
            load();

        } catch (Exception err) {
            JOptionPane.showMessageDialog(null, err);
        }
    }

    private void edit() {
        int konfirmasi = JOptionPane.showConfirmDialog(null, "Apakah anda yakin?", "Konfirmasi", JOptionPane.YES_NO_OPTION);

        if (konfirmasi == JOptionPane.NO_OPTION) {
            return;
        }

        String sql = "UPDATE barang SET nama_barang = ?, kategori = ?, harga = ?, jumlah_stok = ? WHERE id = ?";

        try {
            int selectedRow = jTable1.getSelectedRow();
            String id = jTable1.getValueAt(selectedRow, 0).toString();

            prep = connector.connect.prepareStatement(sql);

            prep.setString(1, namaBarangField.getText());
            prep.setString(2, kategoriField.getText());
            prep.setFloat(3, Float.parseFloat(hargaField.getText()));
            prep.setInt(4, Integer.parseInt(jumlahStokField.getText()));
            prep.setInt(5, Integer.parseInt(id));

            int rowsUpdated = prep.executeUpdate();

            if (rowsUpdated > 0) {
                JOptionPane.showMessageDialog(null, "Data berhasil di update.");
            } else {
                JOptionPane.showMessageDialog(null, "Data gagal di update.");
            }

            load();
        } catch (Exception err) {
            JOptionPane.showMessageDialog(null, err);
        }
    }

    private void delete() {
        int konfirmasi = JOptionPane.showConfirmDialog(null, "Apakah anda yakin?", "Konfirmasi", JOptionPane.YES_NO_OPTION);

        if (konfirmasi == JOptionPane.NO_OPTION) {
            return;
        }

        String sql = "DELETE FROM barang WHERE id = ?";

        try {
            prep = connector.connect.prepareStatement(sql);

            int selectedRow = jTable1.getSelectedRow();
            String id = jTable1.getValueAt(selectedRow, 0).toString();

            prep.setString(1, id);

            int rowsUpdated = prep.executeUpdate();

            if (rowsUpdated > 0) {
                JOptionPane.showMessageDialog(null, "Data berhasil di delete.");
            } else {
                JOptionPane.showMessageDialog(null, "Maaf, data gagal di delete.");
            }

            load();
        } catch (Exception err) {
            JOptionPane.showMessageDialog(null, err);
        }
    }

